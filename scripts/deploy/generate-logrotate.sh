# Generate logrotate configuration from sites.conf
# Usage: sudo ./generate-logrotate.sh
#
# Reads instance directories from /etc/ocms/sites.conf and generates
# /etc/logrotate.d/ocms with per-instance log rotation entries.

set -e

SITES_CONF="/etc/ocms/sites.conf"
LOGROTATE_CONF="/etc/logrotate.d/ocms"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

if [ -f "$SCRIPT_DIR/helper.sh" ]; then
    source "$SCRIPT_DIR/helper.sh"
elif [ -f "/opt/ocms/helper.sh" ]; then
    source "/opt/ocms/helper.sh"
else
    echo_info()  { echo "[INFO] $1"; }
    echo_error() { echo "[ERROR] $1"; }
fi

if [ "$EUID" -ne 0 ]; then
    echo_error "Run as root (use sudo)"
    exit 1
fi

if [ ! -f "$SITES_CONF" ]; then
    echo_error "No sites registered: $SITES_CONF"
    exit 1
fi

# Build logrotate config
cat > "$LOGROTATE_CONF" << 'HEADER'
# oCMS Log Rotation Configuration
# Auto-generated by generate-logrotate.sh from /etc/ocms/sites.conf
# Re-run after adding/removing sites: sudo /opt/ocms/generate-logrotate.sh

HEADER

# Per-instance entries
while IFS=' ' read -r site_id instance_dir user port; do
    [[ "$site_id" =~ ^#.*$ ]] && continue
    [ -z "$site_id" ] && continue

    cat >> "$LOGROTATE_CONF" << EOF
${instance_dir}/logs/*.log {
    su ${user} psaserv
    daily
    rotate 30
    compress
    delaycompress
    missingok
    notifempty
    copytruncate
}

EOF
done < "$SITES_CONF"

# Backup cron log
cat >> "$LOGROTATE_CONF" << 'FOOTER'
# Backup cron log
/var/log/ocms-backup.log {
    su root root
    weekly
    rotate 4
    compress
    delaycompress
    missingok
    notifempty
    create 0640 root root
}
FOOTER

echo_info "Generated $LOGROTATE_CONF"
echo_info "Verify: sudo logrotate -d $LOGROTATE_CONF"
